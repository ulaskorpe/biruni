const slugify = (text,divider = '-') => {
    text = text.toString().toLowerCase().trim();

    // Türkçe karakterleri değiştir
    var turkishCharacters = { 'ğ': 'g', 'ü': 'u', 'ş': 's', 'ı': 'i', 'ö': 'o', 'ç': 'c', 'ə': 'e', 'Ğ': 'G', 'Ü': 'U', 'Ş': 'S', 'İ': 'I', 'Ö': 'O', 'Ç': 'C', 'Ə': 'E' };
    for (var char in turkishCharacters) {
        text = text.replace(new RegExp(char, 'g'), turkishCharacters[char]);
    }

    // URL uygun hale getir
    text = text.replace(/\s+/g, divider) // Boşlukları tireye çevir
        .replace(/[^a-z0-9-]/g, '') // Geçersiz karakterleri kaldır

    return text;
}

